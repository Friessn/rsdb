package pointcloud;

public class Rect2d {
	public final double xmin;
	public final double ymin;
	public final double xmax;
	public final double ymax;

	public Rect2d(double xmin, double ymin, double xmax, double ymax) {
		this.xmin = xmin;
		this.ymin = ymin;
		this.xmax = xmax;
		this.ymax = ymax;
	}

}
